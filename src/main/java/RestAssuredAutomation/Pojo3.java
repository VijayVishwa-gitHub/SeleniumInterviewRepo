package RestAssuredAutomation;

public class Pojo3 {
    private String itemId;;
    private int qty;
    private int price;

    public void setItemId(String itemId){
        this.itemId = itemId;
    }

    public String getItemID(){
        return itemId;
    }

    public void setQty(int qty){
        this.qty = qty;
    }

    public int getQty(){
        return qty;
    }

    public void setPrice(int price){
        this.price = price;
    }

    public int getPrice(){
        return price;
    }


}
